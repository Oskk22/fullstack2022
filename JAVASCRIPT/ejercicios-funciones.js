//EJERCICIO 1 DECLARAR FUNCION FLECHA
const sumar = (x,y) => {
    return x + y;
};
const restar = (x,y) => {
    return x - y;
};
const multiplicar = (x,y) => {
    return x * y;
};
const dividir = (x,y) => {
    return x / y;
};

console.log(sumar(1,2));
console.log(restar(1,2));
console.log(multiplicar(1,2));
console.log(dividir(1,2));

